package com.thing.management.model;

public class Sensor {

	private int id;
	private String sense;
	private String unit;
	private String type;
	
	public int getId() {
		return this.id;
	}
	public String getSense() {
		return this.sense;
	}
	public String getUnit() {
		return this.unit;
	}
	public String getType() {
		return this.type;
	}
	
}
